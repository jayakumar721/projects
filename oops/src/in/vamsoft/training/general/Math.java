package in.vamsoft.training.general;

/**
 * This is a class for mathematical operations.
 * 
 * @author vamsoft
 *
 */
public class Math {

  /**
   * this is a method to sum two integer numbers.
   * 
   * @param a
   *          accepting parameter as integer.
   * @param b
   *          accepting parameter as integer.
   * @return returns sum of two values.
   */

  public int sum(int a, int b) {

    return (a + b);

  }

  /**
   * this is a method to sum two double numbers.
   * 
   * @param a
   *          accepting parameter as double.
   * @param b
   *          accepting parameter as double.
   * @return returns sum of two values.
   */

  public double sum(double a, double b) {

    return (a + b);

  }

}
